package ch3;

public class OperatorEx7 {
    public static void main(String[] args) {
        boolean power = false;
        System.out.println(power);
        power = !power; // power의 값이 false에서 true로 바뀜
        System.out.println(power);
        power = !power;// power의 값이 true에서 false로 바뀜
        System.out.println(power);
    }
}
